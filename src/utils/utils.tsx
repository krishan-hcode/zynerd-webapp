export const buildErrorMessage = (errorObject: any): string => {
  if (typeof errorObject === 'string') {
    return errorObject;
  }
  // network error
  if (errorObject?.statusText?.includes('Network Error')) {
    return errorMessage.NETWORK_ERROR;
  } else if (errorObject?.response?.data?.detail) {
    return errorObject?.response?.data?.detail;
  } else if (errorObject?.response?.data?.message) {
    return errorObject?.response?.data?.message;
  } else if (
    !errorObject?.message ||
    errorObject?.message?.toLowerCase().includes('unexpected')
  ) {
    return errorMessage.SERVER_ERROR;
  } else if (
    errorObject?.message === 'Network request failed' ||
    errorObject?.statusText?.includes('Network Error')
  ) {
    return errorMessage.NETWORK_ERROR;
  } else if (errorObject?.message) {
    return errorObject.message;
  } else {
    return errorMessage.SERVER_ERROR;
  }
};

export const encrypt = (data: string) => {
  try {
    // Create a Forge public key using a PEM format
    const forgePublicKey = forge.pki.publicKeyFromPem(ENCRYPT_PUBLIC_KEY);
    // A random key generated by 16 bytes will use AES-128, 24 => AES-192, 32 => AES-256
    const randomKey = forge.random.getBytesSync(32);
    // An initialization vector (IV) is an arbitrary number that can be used with a secret key for data encryption to foil cyber attacks
    // The initialization vector (IV) converted to decode64
    const iv = forge.util.decode64(ENCRYPT_VECTOR_KEY);
    // A public key is used to encrypt data with RSAES-OAEP
    let encryptedKey = forgePublicKey.encrypt(randomKey.toString(), 'RSA-OAEP');
    // Encrypt some bytes using CBC mode
    const cipher = forge.cipher.createCipher('AES-CBC', randomKey);
    // Cipher starts with an initialization vector (IV)
    cipher.start({iv: iv});
    cipher.update(forge.util.createBuffer(data));
    cipher.finish();
    // Provides encrypted output
    const encrypted = cipher.output;
    // Convert data and keys to encode64
    const encodedData = forge.util.encode64(encrypted.data);
    const encodedKey = forge.util.encode64(encryptedKey);
    return {encodedKey, encodedData};
  } catch (error: any) {
    throw new Error(error.message);
  }
};

