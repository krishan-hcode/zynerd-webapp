export const setGtagUserData = (userData: IUserData) => {
  const address = userData?.address_info?.length
    ? userData?.address_info[0]
    : null;

  window.gtag('set', 'user_data', {
    email: (userData.email ?? '').toLowerCase().trim(),
    phone_number: userData?.phone_number,
    ...(address
      ? {
          address: {
            first_name: (userData?.first_name ?? '').toLowerCase().trim(),
            last_name: (userData?.last_name ?? '').toLowerCase().trim(),
            street: (address?.address1 + ' ' + address?.address2)
              .toLowerCase()
              .trim(),
            city: (address?.city ?? '').toLowerCase().trim(),
            region: (address?.country_state ?? '').toLowerCase().trim(),
            postal_code: ((address?.pincode as string) ?? '')
              .toLowerCase()
              .trim(),
            country: address?.country,
          },
        }
      : {}),
  });
};

// Utility functions for subscription data
export const buildValidityDate = (startAt: string, duration: number) => {
  const endDate = dayjs
    .utc(startAt) // parse in UTC
    .add(duration, 'day') // add duration in days
    .format('DD MMM YYYY');
  const startDate = dayjs
    .utc(startAt) // parse in UTC
    .format('DD MMM YYYY');
  return `${startDate} - ${endDate}`;
};

export const hasAnyActiveSubscription = (
  subscriptions: ISubscriptionSchema[],
) => {
  if (!subscriptions || subscriptions.length === 0) {
    return false;
  }
  return subscriptions?.some(subscription =>
    isSubscriptionActive(subscription.start_at, subscription.duration),
  );
};
export const isSubscriptionActive = (startAt: Date, duration: number) => {
  const endDate = dayjs.utc(startAt).add(duration, 'day');
  return !dayjs().isAfter(endDate);
};

export const triggerAnalyticsEvent = (
  eventName: string,
  params: object = {} as object,
) => {
  try {
    if (!analytics) {
      return;
    }
    logEvent(analytics, eventName, {...params, os: 'WebApp'});
  } catch (error) {
    // Analytics event failed silently
  }
};
